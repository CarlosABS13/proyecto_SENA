// Esta función muestra el indicador de notificaciones
function mostrarIndicador() {
    const indicador = document.querySelector(".notificacion-indicador");
    indicador.style.display = "block"; // Puedes ajustar el estilo según tus preferencias
}

// Esta función oculta el indicador de notificaciones
function ocultarIndicador() {
    const indicador = document.querySelector(".notificacion-indicador");
    indicador.style.display = "none"; // Puedes ajustar el estilo según tus preferencias
}

